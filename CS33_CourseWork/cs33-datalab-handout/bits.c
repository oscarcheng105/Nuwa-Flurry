/* 
 * twosComp2SignMag - Convert from two's complement to sign-magnitude 
 *   where the MSB is the sign bit
 *   You can assume that x > TMin
 *   Example: twosComp2SignMag(-5) = 0x80000005.
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 15
 *   Rating: 4
 */
int twosComp2SignMag(int x) {
    int sign = (x>>31); //check sign of x
    int convert = (~x+1)+(1<<31); //change the sign to unsign type and add the largest negative number to give it a sign.
    return (sign&convert)|(~sign&x); //If negative, check convert, if positive, return x
}
/*
 * bitParity - returns 1 if x contains an odd number of 0's
 *   Examples: bitParity(5) = 0, bitParity(7) = 1
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 20
 *   Rating: 4
 */
int bitParity(int x)
{
    //HINT from TA (RIOS, VICTOR)
    //Since even ^ even = even, even ^ odd = odd, odd ^ odd = even
    int half1 = x^(x>>16); //Separate x's bits into two and XOR xthem to see if x's count of 0s are even or odd
    int half2 = half1^(half1>>8); //Shrink to last bit
    int half3 = half2^(half2>>4);
    int half4 = half3^(half3>>2);
    int half5 = half4^(half4>>1);
    return (half5&1); //If last bit is 1, then x contains odd number of 0's
}

