/* 
 * isLess - if x < y  then return 1, else return 0 
 *   Example: isLess(4,5) = 1.
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 24
 *   Rating: 3
 */
int isLess(int x, int y) {
    int sign =!((x^y)>>31); //See if x and y is the same sign, return 1 for same, 0 for diff.
    int sameSign = !(((x+~y+1)^~0)>>31); //Same sign, see if (x-y) is negative, return 1 if negative
    int diffSign = !((x^~0)>>31); //Diff sign, see if x is negative, return 1 if negative
    return  (sign&sameSign)|(~sign&diffSign); //If a returns same sign, use method b, diff sign use method c
}
/*
 * multFiveEighths - multiplies by 5/8 rounding toward 0.
 *   Should exactly duplicate effect of C expression (x*5/8),
 *   including overflow behavior.
 *   Examples: multFiveEighths(77) = 48
 *             multFiveEighths(-22) = -13
 *             multFiveEighths(1073741824) = 13421728 (overflow)
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 12
 *   Rating: 3
 */

int multFiveEighths(int x) {
    int multiply = (x<<2)+x; //Mult by five
    int sign = (x>>31); //See if positive or negative
    int negDivide = (multiply+7)>>3; //Divide by 8, add 7 to a to fix rounding (bias term);
    int posDivide = multiply>>3; //Divide by 8
    return (sign&negDivide)|(~sign&posDivide); //If negative use method c, if positive, use method d
}
