/* 
 * isEqual - return 1 if x == y, and 0 otherwise 
 *   Examples: isEqual(5,5) = 1, isEqual(4,5) = 0
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 5
 *   Rating: 2
 */
int isEqual(int x, int y) {
    /*
     Since (x^y) gives 0 when x and y is same
     and ! gives 1 when (x^y) is zero and 0 when (x^y) is not zero
     !(x^y) gives 1 when x and y is same, and 0 when x and y is diff.
     */
    return !(x^y);
}
/* 
 * fitsBits - return 1 if x can be represented as an 
 *  n-bit, two's complement integer.
 *   1 <= n <= 32
 *   Examples: fitsBits(5,3) = 0, fitsBits(-4,3) = 1
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 15
 *   Rating: 2
 */
int fitsBits(int x, int n){
    int negative = ((x>>(n+~0))+1);
    /*
     returns 0 for valid negative num range
     returns additional -1 for each negative number that exceeds one bit
     returns 1 for valid positive num range
     returns additional +1 for each positive number that exceeds one bit
     */
    int positive = ((~x>>(n+~0))>>1);
    /*
     returns 0 for valid positive num range
     returns additional -1 for each positive number that exceeds one bit
     returns 1 for valid negative num range
     returns additional +1 for each negative number that exceeds one bit
     */
    return !(negative+positive); //If sum of a+b is 0, then return 1
}

