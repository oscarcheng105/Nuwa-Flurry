/* 
 * bitNor - ~(x|y) using only ~ and & 
 *   Example: bitNor(0x6, 0x5) = 0xFFFFFFF8
 *   Legal ops: ~ &
 *   Max ops: 8
 *   Rating: 1
 */
int bitNor(int x, int y) {
    /*
     ~(x|y) finds OR first then gives its complement
     Finding AND in two complemented integers gives OR in complementary form
     so (~x)&(~y) gives ~(x|y)
     */
    return ~x&~y;
}
